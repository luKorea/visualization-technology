function mapDataKey(arr, key) {
  const obj = {};
  arr.forEach((item) => {
    let value = item[key];
    obj[value] ? obj[value].push(item) : (obj[value] = [item]);
  });
  return Object.keys(obj).map((item) => {
    return {
      [item]: obj[item]
    }
  });
}

